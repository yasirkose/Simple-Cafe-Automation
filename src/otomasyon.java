    public void button_olusturMasa()
    {
        int sutun=4;
        int satir;
        if(masaSayisi/sutun==0) satir=masaSayisi/4;
        else satir=(masaSayisi/4)+1;
        if(satir==1) satir=satir+1;
        GridLayout experimentLayout = new GridLayout(sutun,satir,15,15);
        jPanel1.setLayout(experimentLayout);
        
        for(int i=1;i<=masaSayisi;i++)
        {
            JButton button=new JButton("masa"+i);
            button.setText("Masa "+String.valueOf(i));
            Font myFont = new Font("Tahoma", Font.PLAIN,14);
            
            button.addActionListener(new ActionListener(){
                public void actionPerformed(ActionEvent e) {
                    Object obj = e.getSource();
                    for(int i=0;i<butonlar.size();i++)
                    {
                        if(obj==butonlar.get(i))
                        {
                            secilen_masa=i+1;
                            vt_hesapTabloyaYaz();
                            jPanel1.revalidate();
                            jPanel1.repaint();
                            repaint();
                            break;
                        }
                    }
                }}
            );
            
            button.setFont(myFont);
            jPanel1.add(button);
            butonlar.add(button);
        }
        jPanel1.revalidate();
        jPanel1.repaint();
        repaint();
    }
    
    
    
