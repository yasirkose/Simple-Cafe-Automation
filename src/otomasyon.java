    public void vt_hesapTabloyaYaz()
    {
        if(secilen_masa!=0)
        {
            jLabel3.setText("Masa "+secilen_masa);
        }
        else jLabel3.setText("");
        Connection con;
        con = null;
        try
        {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver"); //Bağlantı nesnesi
            String strdatabase = "jdbc:ucanaccess://otomasyonVT.accdb"; //veritabanının ismi
            con = DriverManager.getConnection(strdatabase); //Access-java bağlantısnı sağlayan jdbc sürücüsü ile bağlantı oluşturuluyor
        }
        catch(Exception e)
        {        
            System.out.println("bağlantı hatası");     
        }
        String strSQL ="select * from masa"+secilen_masa;
        try {
            Statement stmt = con.createStatement();
            Object[][] obj;
            
            ResultSet rs = stmt.executeQuery("SELECT COUNT(*) AS rowcount FROM masa"+secilen_masa);
            rs.next();
            int iCount = rs.getInt("rowcount") ;
            rs.close() ;
            
            rs = stmt.executeQuery(strSQL);

            obj= new Object [iCount][3];
            if(iCount==0)
            {
                buton_hesapSil.setVisible(false);
                masa_renk(0);
            }
            else
            {
                buton_hesapSil.setVisible(true);
                masa_renk(1);
            }

            iCount = 0;
            double toplam_hesap=0;
            while ( rs.next() ) 
            {
                obj[iCount][0] = rs.getString("Urun");
                obj[iCount][1] = rs.getInt("Adet");
                obj[iCount][2] = rs.getDouble("Fiyat")+" TL";
                toplam_hesap=toplam_hesap+(rs.getInt("Adet")*rs.getDouble("Fiyat"));
                iCount = iCount+1;
            }
            if(toplam_hesap==0)
            {
                label_toplamHesap.setVisible(false);
            }
            else
            {
                label_toplamHesap.setVisible(true);
                label_toplamHesap.setText("HESAP: "+String.valueOf(toplam_hesap) +" TL");
            }
            DefaultTableModel model=new javax.swing.table.DefaultTableModel(obj,new String [] {"Ürün", "Adet", "Birim Fiyat"})
            {
                @Override
                public boolean isCellEditable(int row, int column) {
                return false;
                }
            };
            
            jTable1.setModel(model);
            jTable1.setUpdateSelectionOnSort(false);
            rs.close() ; 
            
            con.close();
        } 
        catch (SQLException ex) 
        {
            System.out.println("Statement Hatası");
        }
        jPanel1.revalidate();
        jPanel1.repaint();
        repaint();
    }
    
